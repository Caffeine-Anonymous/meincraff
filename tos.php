</p>

<h2>Build rules</h2>

<h3>TNT</h3>
<p id="tnt">
    TNT is not banned, It's not deactivated, because there are legitimate 
    uses for it. However, excessive use of TNT is discouraged,
    because destruction of the landscape is still considered greifing.
</p>

<h3>Survival Maps</h3>
<p id="survival">
    Survival worlds are SURVIVAL only.
    Also, clear your damn inventory when you switch worlds
</p>

<h3>Animal Spawning</h3>
<p id="animals">
    Spawning animals and mobs is fine. 
    Spawning a bunch of them in one location will crash the server, 
    which is not fine. Therefore making an animal spawner which 
    intentionally does this is a bannable offense.
</p>

<h3>Clipboard Clearing</h3>
<p id="clipboard">
    Dunjin realm is not for worldedit clipboard clearing,
    contrary to popular belief. If you have a world edit clipboard you
    want to keep, find a legitimate home for it.
</p>

<h3>Towers and Tunnels</h3>
<p id="towering">
    No 1x1 towers or 2x1 tunnels.
    They look like crap and you shouldn't need to use them 
    since the overworld is creative. You're not allowed to do them in
    Survival either.
</p>

<h3>Pixel Art</h3>
<p id="pixel-art">
    No Pixel art. There are plenty of servers for this,
    We are not one of them. 
</p>

<h2>City Planning</h2>

<h3>Rails</h3>
<p id="rails">
    We have rail systems that span the entire map, 
    if you build a good city, we will link you to one of them.
    If you want to build your own rail system, that's cool too,
    just let us know before you decide to go "nationwide"
    so we can plan out an easement for you.
</p>

<h3>Structural Support</h3>
<p id="structures">
    Structures should be built logically and with structural support.
    Unless you're planning on making a flying city, there should be a 
    foundation, supports, archetectual considerations, and aesthetic
    appeal in your builds. This applies on all maps on the server.
    If your builds look terrible (made from stone/dirt/etc) or is built
    inside someone else's city without permission, your build may be moved
    or removed to keep the server looking nice. 
</p>

<h3>New Cities</h3>
<p id="cities">
    If you want to build a city, let us help you choose a location.
    We have tons of space but city planning is essential into keeping
    the map organized. You may not realize just how close you are to 
    someone else's stuff until you're building on top of it.
</p>

<h4>City features</h4>
<p id="city-features">
    Cities have boaders, names, and points of interest.
    Your city doesn't have to be a grid, but having plots availible for
    public build projects is a good idea. Having a welcome center or Town
    Hall is generally a good idea as well, since people will eventually wonder
    what your city is when you're not online.
</p>
<h3>Building in Cities</h3>
<p id="city-building">
    Feel free to help us on city builds, just remember that even though 
    most of them are community built, you should still ask before 
    modifying existing structures or taking an empty lot inside a city's walls.
</p>
